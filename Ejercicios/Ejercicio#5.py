#5. Crear una función que convierta grados Fahrenheit a grados Celsius.
Grados_Fahrenheit = int(input("Ingrese un número para grados en Fahrenheit:"))

Grados_Celsius = (Grados_Fahrenheit - 32) * 5.0/9.0

print("Sus grados en Fahrenheit son:", Grados_Fahrenheit,"°", "En celsius son:", Grados_Celsius,"°")