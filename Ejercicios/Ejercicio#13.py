#13. Crear un programa que pida al usuario ingresar dos números y calcule su suma, resta, multiplicación y división.
numero1=int(input("Ingrese su primer número:"))
numero2=int(input("Ingrese su segundo número:"))

suma= numero1 + numero2
print("Su suma de dos números es:", suma)

resta= numero1 - numero2
print("Su resta de dos números es:", resta)

multiplicacion= numero1 * numero2
print("Su multiplicación de dos números es:", multiplicacion)

division= numero1/numero2
print("Su división de dos números es:", division)